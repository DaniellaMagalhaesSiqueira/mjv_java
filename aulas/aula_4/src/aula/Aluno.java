package aula;

public class Aluno {

	int matricula;
	String nome;
	int id;
	String dataNAscimento;
	String rg;
	String sexo;
	
	public Aluno() {}
	
	public Aluno(int matricula, String nome, int id, String dataNAscimento, String rg, String sexo) {
		super();
		this.matricula = matricula;
		this.nome = nome;
		this.id = id;
		this.dataNAscimento = dataNAscimento;
		this.rg = rg;
		this.sexo = sexo;
	}
	
	
}
